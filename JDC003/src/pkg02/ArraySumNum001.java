package pkg02;

public class ArraySumNum001 {
	public static void main(String args[]){
		int ar[]=new int[2];
		
		for(int i=0;i<10;i++){
			for(int a=0;a<10;a++){
				ar[0]=a;
				for(int b=0;b<10;b++){
					ar[1]=b;
					for(int c=0;c<1;c++){
						System.out.println(ar[0]+"\t"+ar[1]);
				}
			
				}
			}
			
		}
	}
	
}
