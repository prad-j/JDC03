package pkg02;

public class SwitchCase002 {
	public static void main(String args[]){
		int a =2;
		
		switch (a){
		case 1:
			System.out.println("a is 1");
			break;
		case 2:
			System.out.println("a is 2");
			break;
		case 3:
			System.out.println("a is 3");
			break;
		default:
			System.out.println("default");
			break;
		}
	}
}
